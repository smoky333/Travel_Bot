from aiogram import Router                  # –†–æ—É—Ç–µ—Ä - —ç—Ç–æ –∫–∞–∫ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–∏—Ç–µ–ª—å –∑–∞–¥–∞—á –¥–ª—è —Ä–∞–∑–Ω—ã—Ö –∫–æ–º–∞–Ω–¥
from aiogram.filters import CommandStart    # –§–∏–ª—å—Ç—Ä, –∫–æ—Ç–æ—Ä—ã–π –ª–æ–≤–∏—Ç –∏–º–µ–Ω–Ω–æ –∫–æ–º–∞–Ω–¥—É /start
from aiogram.types import Message           # Message - —ç—Ç–æ –æ–±—ä–µ–∫—Ç —Å–æ–æ–±—â–µ–Ω–∏—è, –∫–æ—Ç–æ—Ä–æ–µ –ø—Ä–∏—Å–ª–∞–ª –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å

# –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—ã–π "—Ä–∞—Å–ø—Ä–µ–¥–µ–ª–∏—Ç–µ–ª—å" —Ç–æ–ª—å–∫–æ –¥–ª—è –∫–æ–º–∞–Ω–¥ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
# –ú—ã –¥–∞–¥–∏–º –µ–º—É –∏–º—è, —á—Ç–æ–±—ã –ø–æ—Ç–æ–º –≤ main.py –±—ã–ª–æ –ø–æ–Ω—è—Ç–Ω–æ, —á—Ç–æ —ç—Ç–æ –∑–∞ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–∏—Ç–µ–ª—å
user_commands_router = Router(name="user_commands_router")

# –≠—Ç–æ "–¥–µ–∫–æ—Ä–∞—Ç–æ—Ä". –û–Ω –≥–æ–≤–æ—Ä–∏—Ç: "–≠–π, —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–∏—Ç–µ–ª—å! –ï—Å–ª–∏ –ø—Ä–∏–¥–µ—Ç –∫–æ–º–∞–Ω–¥–∞ /start, –≤—ã–ø–æ–ª–Ω–∏ —Ñ—É–Ω–∫—Ü–∏—é –Ω–∏–∂–µ"
@user_commands_router.message(CommandStart())
async def handle_start_command(message: Message):
    # message.from_user —Å–æ–¥–µ—Ä–∂–∏—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ, –∫–æ—Ç–æ—Ä—ã–π –Ω–∞–ø–∏—Å–∞–ª –±–æ—Ç—É
    user_name = message.from_user.full_name  # –ü–æ–ª—É—á–∞–µ–º –ø–æ–ª–Ω–æ–µ –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    user_id = message.from_user.id            # –ü–æ–ª—É—á–∞–µ–º –µ–≥–æ —É–Ω–∏–∫–∞–ª—å–Ω—ã–π ID –≤ Telegram

    # –§–æ—Ä–º–∏—Ä—É–µ–º –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ. –ò—Å–ø–æ–ª—å–∑—É–µ–º f-—Å—Ç—Ä–æ–∫—É –¥–ª—è –≤—Å—Ç–∞–≤–∫–∏ –∏–º–µ–Ω–∏.
    # <b>...</b> —Å–¥–µ–ª–∞–µ—Ç —Ç–µ–∫—Å—Ç –∂–∏—Ä–Ω—ã–º (–º—ã –∂–µ —É–∫–∞–∑–∞–ª–∏ ParseMode.HTML –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –±–æ—Ç–∞)
    text = f"–ü—Ä–∏–≤–µ—Ç, <b>{user_name}</b>! üëã\n–Ø —Ç–≤–æ–π –ø–µ—Ä—Å–æ–Ω–∞–ª—å–Ω—ã–π Travel Bot.\n–ì–æ—Ç–æ–≤ –ø–æ–º–æ—á—å —Å–ø–ª–∞–Ω–∏—Ä–æ–≤–∞—Ç—å —Ç–≤–æ–µ –ª—É—á—à–µ–µ –ø—É—Ç–µ—à–µ—Å—Ç–≤–∏–µ!\n\n–ß—Ç–æ–±—ã –Ω–∞—á–∞—Ç—å, –∏—Å–ø–æ–ª—å–∑—É–π –∫–æ–º–∞–Ω–¥—É /plan_trip (–Ω–æ –æ–Ω–∞ –ø–æ–∫–∞ –Ω–µ —Ä–∞–±–æ—Ç–∞–µ—Ç üòâ)."

    # –û—Ç–≤–µ—á–∞–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é —ç—Ç–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ–º
    await message.answer(text)

    # –ú–æ–∂–Ω–æ —Ç–∞–∫–∂–µ –æ—Ç–ø—Ä–∞–≤–∏—Ç—å —á—Ç–æ-—Ç–æ –≤ –∫–æ–Ω—Å–æ–ª—å –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏ (–Ω–µ–æ–±—è–∑–∞—Ç–µ–ª—å–Ω–æ)
    print(f"–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å {user_name} (ID: {user_id}) –Ω–∞–∂–∞–ª /start")